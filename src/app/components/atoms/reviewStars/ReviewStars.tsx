export interface IReviewStars {
  rating: number;
  isSmall?: boolean;
  isMed?: boolean;
  gap?: number;
}

export default function ReviewStars({
  rating,
  isSmall,
  isMed,
  gap,
}: IReviewStars) {
  const getStars = Math.round(rating);
  const star = () =>
    isSmall ? smallColoredStar() : isMed ? medColoredStar() : coloredStar();

  return (
    <div
      style={{
        gap,
      }}
      className={styles.review_stars}
    >
      {Array.from({ length: 5 }).map((_, index) => (
        <div
          className={`${styles.stars__item} ${
            index >= getStars ? styles.item__inactive : ""
          }`}
        >
          {star()}
        </div>
      ))}
    </div>
  );
}
